export const MIN_VERSION = 1;
export const MAX_VERSION = 40;

/** Number of data codewords available for a given version and level. */
export function dataCodewordCount(version, level) {
    const [, blocks1, data1, blocks2, data2] = BLOCK_LAYOUT[level][version];
    return blocks1 * data1 + blocks2 * data2;
}

/** Module count along one edge of the symbol. */
export function moduleSize(version) {
    return version * 4 + 17;
}

/** Bits used by the byte-mode character-count indicator at a given version. */
export function byteCountBits(version) {
    return version <= 9 ? 8 : 16;
}
